#include <ft.h>

void	ufputn(const int fd, int n)
{
	if (n < 0)
	{
		ufputc('-', fd);
		n = -n;
	}
	if (n == -2147483648)
	{
		ufputc('2', fd);
		n %= 1000000000;
		n = -n;
	}
	if (n >= 10)
	{
		ufputn(n / 10, fd);
		ufputn(n % 10, fd);
	}
	else
		ufputc(n + '0', fd);
}
