#include <ft_string.h>

char	*ft_basename(char *s)
{
	size_t i;

	if (!s || !*s)
		return (".");
	i = ft_strlen(s) - 1;
	while (i && s[i] == '/')
	{
		s[i] = '\0';
		i--;
	}
	while (i && s[i - 1] != '/')
		i--;
	return (s + i);
}
