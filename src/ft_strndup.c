#include "libft.h"

char	*ft_strndup(const char *s1, size_t n)
{
	char	*s2;

	if (!(n + 1) || !(s2 = ft_strnew(n)))
		return (NULL);
	ft_strncpy(s2, s1, n);
	return (s2);
}
