#include <uio.h>

void	ufputn(t_fd fd, int n)
{
	if (n < 0)
	{
		ufputc(fd, '-');
		n = -n;
	}
	if (n == -2147483648)
	{
		ufputc(fd, '2');
		n %= 1000000000;
		n = -n;
	}
	if (n >= 10)
	{
		ufputn(fd, n / 10);
		ufputn(fd, n % 10);
	}
	else
		ufputc(fd, n + '0');
}
