#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t len)
{
	char		*d;
	const char	*s;
	char		*lastd;
	char		*lasts;

	if (!dst && !src)
		return (NULL);
	d = dst;
	s = src;
	if (d < s)
		while (len--)
			*d++ = *s++;
	else
	{
		lasts = (char *)s + len - 1;
		lastd = d + len - 1;
		while (len--)
			*lastd-- = *lasts--;
	}
	return (dst);
}
